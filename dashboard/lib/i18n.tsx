export type Translations = typeof en;
const translations: Record<Lang, Translations> = { en, fr };

interface LangCtx {
  lang: Lang;
  t: Translations;
  setLang: (l: Lang) => void;
}

const LanguageContext = createContext<LangCtx>({ lang: 'en', t: en, setLang: () => {} });

export function LanguageProvider({ children }: { children: ReactNode }) {
  // Always start with 'en' on both server and client to avoid hydration mismatch.
  // useEffect reads localStorage after hydration is complete.
  const [lang, setLangState] = useState<Lang>('en');

  useEffect(() => {
    const stored = localStorage.getItem('yl-lang') as Lang | null;
    if (stored === 'fr') setLangState('fr');
  }, []);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    localStorage.setItem('yl-lang', l);
  }, []);

  return (
    <LanguageContext.Provider value={{ lang, t: translations[lang], setLang }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLang() {
  return useContext(LanguageContext);
}
